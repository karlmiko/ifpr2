package ifpr.computador;

public class Computador 
{
	private int id;
	private String marca;
	private int qtdMemoriaRam;
	
	public int getId() 
	{
		return id;
	}
	
	public void setId(int id)
	{
		this.id = id;
	}
	
	public String getMarca()
	{
		return marca;
	}
	
	public void setMarca(String marca) 
	{
		this.marca = marca;
	}
	
	public int getQtdMemoriaRam() 
	{
		return qtdMemoriaRam;
	}
	
	public void setQtdMemoriaRam(int qtdMemoriaRam)
	{
		this.qtdMemoriaRam = qtdMemoriaRam;
	}
}

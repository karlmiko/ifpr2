package funcionario;

public class Funcionario
{
		private int ramal;
		private String nome;
		private String cargo;
		
		public int getRamal() 
		{
			return ramal;
		}
		
		public void setRamal(int ramal) 
		{
			this.ramal = ramal;
		}
		
		public String getNome()
		{
			return nome;
		}
		
		public void setNome(String nome) 
		{
			this.nome = nome;
		}
		
		public String getCargo() 
		{
			return cargo;
		}
		
		public void setCargo(String cargo) 
		{
			this.cargo = cargo;
		}
	}
package sala;

public class Sala 
{	
	private int sala;
	private int capacidade;

	public int getSala() 
	{
		return sala;
	}

	public void setSala(int sala) 
	{
		this.sala = sala;
	}

	public int getCapacidade() 
	{
		return capacidade;
	}

	public void setCapacidade(int capacidade)
	{
		this.capacidade = capacidade;
	}
}